package com.While_DoWhile;

public class WhileDemo {

    public static void main(String[] args) {
        int i=5;
        int j =5;
        while(i>0){
            System.out.println(i);
            i--;
        }

        do{
            System.out.println(j);
            j--;
        }while(j>0);
    }
}
